--Q1. Total sales revenue generated by each product
SELECT 
p.product_id,
p.product_name,
COUNT(s.sale_id) AS total_units_sold,
SUM(p.price) AS total_revenue
FROM Sales s
JOIN Product p ON s.product_id = p.product_id
GROUP BY p.product_id, p.product_name
ORDER BY total_revenue DESC;

--Q2. Top 3 products by highest sales revenue
SELECT TOP 3
p.product_id,
p.product_name,
COUNT(*) AS units_sold,
SUM(p.price) AS revenue
FROM Sales s
JOIN Product p ON s.product_id = p.product_id
GROUP BY p.product_id, p.product_name
ORDER BY revenue DESC;

--Q3. How many users are Gold members?
SELECT COUNT(*) AS total_gold_users
FROM Goldusers_Signup;


--Q4. Revenue from Gold users
SELECT 
SUM(p.price) AS revenue_from_gold_users
FROM Sales s
JOIN Goldusers_Signup g ON s.user_id = g.userid
JOIN Product p ON s.product_id = p.product_id;

--Q5. Total orders by Gold users
SELECT 
COUNT(*) AS total_orders_by_gold_users
FROM Sales s
JOIN Goldusers_Signup g ON s.user_id = g.userid;

--Q6. Duration of Gold membership per user
SELECT
g.userid,
g.gold_signup_date,
DATEDIFF(DAY, g.gold_signup_date, GETDATE()) AS days_as_gold,
DATEDIFF(MONTH, g.gold_signup_date, GETDATE()) AS months_as_gold,
DATEDIFF(YEAR, g.gold_signup_date, GETDATE()) AS years_as_gold
FROM Goldusers_Signup g
ORDER BY g.userid;

--Q7. Most popular product among Gold users
SELECT TOP 1 
p.product_id,
p.product_name,
COUNT(*) AS times_ordered
FROM Sales s
JOIN Goldusers_Signup g ON s.user_id = g.userid
JOIN Product p ON s.product_id = p.product_id
GROUP BY p.product_id, p.product_name
ORDER BY times_ordered DESC;

--Q8. Total revenue per year
SELECT 
YEAR(created_date) AS year,
SUM(p.price) AS total_revenue
FROM Sales s
JOIN Product p ON s.product_id = p.product_id
GROUP BY YEAR(created_date)
ORDER BY year;

-- Q9. How has the sales revenue changed over the years?
WITH yearly AS (SELECT YEAR(s.created_date) AS year, SUM(p.price) AS revenue
FROM Sales s
JOIN Product p ON s.product_id = p.product_id
GROUP BY YEAR(s.created_date))
SELECT
year,
revenue,
revenue - LAG(revenue) OVER (ORDER BY year) AS absolute_change,
CASE 
WHEN LAG(revenue) OVER (ORDER BY year) = 0 THEN NULL
ELSE ROUND((revenue - LAG(revenue) OVER (ORDER BY year)) * 100.0 
/ LAG(revenue) OVER (ORDER BY year), 2
)
END AS percent_change
FROM yearly;

-- Q10. What percentage of users are Gold members compared to total users?
SELECT
(SELECT COUNT(*) FROM Goldusers_Signup) AS gold_users,
(SELECT COUNT(*) FROM Users) AS total_users,
CAST((SELECT COUNT(*) FROM Goldusers_Signup) * 100.0 / 
(SELECT COUNT(*) FROM Users)
AS DECIMAL(5,2)) AS percent_gold;

-- Q11. How many orders have gold members placed, and how much have they spent?
SELECT
g.userid,
COUNT(s.sale_id) AS total_orders,
SUM(p.price) AS total_spent
FROM Goldusers_Signup g
LEFT JOIN Sales s ON g.userid = s.user_id
LEFT JOIN Product p ON s.product_id = p.product_id
GROUP BY g.userid
ORDER BY total_spent DESC;

-- Q12. What is the total amount each customer spent on online food delivery?
SELECT
u.userid,
un.user_name,
COUNT(s.sale_id) AS total_orders,
SUM(p.price) AS total_spent
FROM Users u
LEFT JOIN user_name un ON u.userid = un.userID
LEFT JOIN Sales s ON u.userid = s.user_id
LEFT JOIN Product p ON s.product_id = p.product_id
GROUP BY u.userid, un.user_name
ORDER BY total_spent DESC;

-- Q13. What is the frequency of customer visits to the online platform?
SELECT
u.userid,
un.user_name,
COUNT(s.sale_id) AS total_orders,
COUNT(DISTINCT s.created_date) AS visit_days
FROM Users u
LEFT JOIN user_name un ON u.userid = un.userID
LEFT JOIN Sales s ON u.userid = s.user_id
GROUP BY u.userid, un.user_name
ORDER BY total_orders DESC;

-- Q14. What was the first order purchased by each customer?
WITH first_order AS (
SELECT user_id, MIN(created_date) AS first_date
FROM Sales
GROUP BY user_id)
SELECT
f.user_id,
un.user_name,
f.first_date,
p.product_id,
p.product_name
FROM first_order f
JOIN Sales s ON s.user_id = f.user_id AND s.created_date = f.first_date
JOIN Product p ON s.product_id = p.product_id
LEFT JOIN user_name un ON f.user_id = un.userID;

-- Q15. What is the most purchased item on the menu and how many times was it purchased?
SELECT TOP 1
p.product_id,
p.product_name,
COUNT(*) AS times_ordered
FROM Sales s
JOIN Product p ON s.product_id = p.product_id
GROUP BY p.product_id, p.product_name
ORDER BY times_ordered DESC;

-- Q16. Which item was most popular for each customer?
WITH cte AS (SELECT 
s.user_id,
s.product_id,
COUNT(*) AS cnt,ROW_NUMBER() OVER (PARTITION BY s.user_id ORDER BY COUNT(*) DESC) AS rn
FROM Sales s
GROUP BY s.user_id, s.product_id)
SELECT 
cte.user_id,
un.user_name,
cte.product_id,
p.product_name,
cte.cnt AS times_ordered
FROM cte
JOIN Product p ON cte.product_id = p.product_id
LEFT JOIN user_name un ON cte.user_id = un.userID
WHERE rn = 1
ORDER BY cte.user_id;

-- Q17. Which item was purchased first after the customer became a Gold member?
WITH first_after AS (SELECT 
g.userid,
MIN(s.created_date) AS first_after_date
FROM Goldusers_Signup g
JOIN Sales s ON g.userid = s.user_id
WHERE s.created_date >= g.gold_signup_date
GROUP BY g.userid)
SELECT
f.userid,
un.user_name,
f.first_after_date,
p.product_id,
p.product_name
FROM first_after f
JOIN Sales s ON s.user_id = f.userid AND s.created_date = f.first_after_date
JOIN Product p ON s.product_id = p.product_id
LEFT JOIN user_name un ON f.userid = un.userID;

-- Q18. Which item was purchased before the customer became a Gold member?
WITH last_before AS (SELECT 
g.userid,
MAX(s.created_date) AS last_before_date
FROM Goldusers_Signup g
JOIN Sales s ON g.userid = s.user_id
WHERE s.created_date < g.gold_signup_date
GROUP BY g.userid)
SELECT
l.userid,
un.user_name,
l.last_before_date,
p.product_id,
p.product_name
FROM last_before l
JOIN Sales s ON s.user_id = l.userid AND s.created_date = l.last_before_date
JOIN Product p ON s.product_id = p.product_id
LEFT JOIN user_name un ON l.userid = un.userID;

-- Q19. What is the total number of orders and total amount spent before becoming a Gold member?
SELECT
g.userid,
un.user_name,
COUNT(s.sale_id) AS orders_before_gold,
SUM(p.price) AS amount_before_gold
FROM Goldusers_Signup g
LEFT JOIN Sales s ON g.userid = s.user_id AND s.created_date < g.gold_signup_date
LEFT JOIN Product p ON s.product_id = p.product_id
LEFT JOIN user_name un ON g.userid = un.userID
GROUP BY g.userid, un.user_name
ORDER BY g.userid;

-- Q20. Rank all transactions during Gold membership; mark non-gold as 'NA'
SELECT
s.sale_id,
s.user_id,
un.user_name,
s.created_date,
p.product_name,
CASE 
WHEN s.created_date >= g.gold_signup_date THEN 
ROW_NUMBER() OVER (PARTITION BY s.user_id
ORDER BY s.created_date)
ELSE 'NA'
END AS gold_rank
FROM Sales s
LEFT JOIN Goldusers_Signup g ON s.user_id = g.userid
LEFT JOIN Product p ON p.product_id = s.product_id
LEFT JOIN user_name un ON un.userID = s.user_id
ORDER BY s.user_id, s.created_date;


-- Create database
CREATE DATABASE CustomerBehavior;
USE CustomerBehavior;

--Create tables
CREATE TABLE dbo.Product
(product_id INT NOT NULL PRIMARY KEY,
product_name NVARCHAR(200) NOT NULL,
price DECIMAL(10,2) NOT NULL);

-- user_name: userID (PK), user_name
CREATE TABLE dbo.user_name
(userID INT NOT NULL PRIMARY KEY,
user_name NVARCHAR(200) NOT NULL);

-- Users: userid (PK), signup_date
CREATE TABLE dbo.Users
(userid INT NOT NULL PRIMARY KEY,
signup_date DATE NOT NULL);

-- Goldusers_Signup: userid (PK, FK), gold_signup_date
CREATE TABLE dbo.Goldusers_Signup
(userid INT NOT NULL PRIMARY KEY,
gold_signup_date DATE NOT NULL,
CONSTRAINT FK_GoldUsers_User FOREIGN KEY (userid) REFERENCES dbo.Users(userid));

-- Sales: sale_id PK, user_id FK, created_date, product_id FK
CREATE TABLE dbo.Sales
(sale_id INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
user_id INT NOT NULL,
created_date DATE NOT NULL,
product_id INT NOT NULL,
CONSTRAINT FK_Sales_User FOREIGN KEY (user_id) REFERENCES dbo.Users(userid),
CONSTRAINT FK_Sales_Product FOREIGN KEY (product_id) REFERENCES dbo.Product(product_id));


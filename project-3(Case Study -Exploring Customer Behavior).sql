s.sale_id,
s.user_id,
un.user_name,
s.created_date,
p.product_name,
CASE 
WHEN s.created_date >= g.gold_signup_date THEN 
CAST(ROW_NUMBER() OVER (PARTITION BY s.user_id
ORDER BY s.created_date) AS VARCHAR(10))
ELSE 'NA'
END AS gold_rank
FROM Sales s
LEFT JOIN Goldusers_Signup g ON s.user_id = g.userid
LEFT JOIN Product p ON p.product_id = s.product_id
LEFT JOIN user_name un ON un.userID = s.user_id
ORDER BY s.user_id, s.created_date;


